          <h1>How To Become A Driving Instructor</h1>
          <p>Training to become a Driving Instructor takes three main steps. We have outlined these below to make the
            process as simple as we can. When you train to be a qualified Driving Instructor with PassVeryFast Driving School a
            member of our Training Team will be on hand to offer support and advice throughout.</p>
          <h1 style = "background-color:gray; ">Step 1: Pre-qualifications and applications</h1>
          <p>In order to begin training to become a Driving Instructor, you must meet certain criteria as set by the
            <span class="caps">DVSA </span>– Driver and Vehicle Standards Agency.</p>
          <p>You must:</p>
          <ul>
            <li>Be at least 21 years old</li>
            <li>Have held a full driving licence for at least 3 years.</li>
          </ul>
          <p>If you meet the above criteria then you can start the process of becoming a Driving Instructor.</p>
          <h3>Criminal Record Check</h3>
          <p>Firstly, you will need to have a criminal record check. You must apply for one even if you hold one
            already. There are two ways to do this:</p>
          <h4>Online</h4>
          <p>You can apply online by filling out an application in your browser.</p>
          <p>After filling out your online application you must prove your identity. This can be done at a post office
            that offers the ‘CRB and ID verification’ service and will cost £6. You must provide 3 original forms of
            identification, these could be:</p>
          <ul>
            <li>Your driving licence</li>
            <li>Your passport</li>
            <li>A utility bill that is less than 3 months old</li>
            <li>A bank statement that is less than 3 months old</li>
          </ul>
          <p>These documents must be originals, copies will not be accepted. You must also bring out your printed
            application – the page with the barcodes on.</p>
          <h4>Post</h4>
          <p>To apply by post you need to fill out this form and a postal application will be sent to you within 5
            working days.</p>
          <p><span class="caps">DVSA </span>Online <span class="caps">CRB </span>Team<br />
            GBGroup<br />
            1 Wilford Business Park<br />
            Ruddington Lane<br />
            Nottingham<br />
            <span class="caps">NG11 7EP</span>
          </p>
          <p>For postal applications checks via the post office checking service are available at a cost of £8.50. For
            applications sent to the GBGroup, the cost is £11.80.</p>
          <h3><span class="caps">ADI </span>Application Process</h3>
          <p>Once you have received your criminal records check, you can start the application process to become a
            Driving Instructor.</p>
          <p>To apply you must have:</p>
          <ul>
            <li>Your driving licence number</li>
            <li>Any details of driving disqualifications, motoring or non-motoring offences</li>
            <li>Any information on court cases that are being brought against you.</li>
          </ul>
          <p>You can apply here.</p>
          <h4>Motoring Offences and Convictions</h4>
          <p>You may have your application refused if the <span class="caps">ADI</span> registrar feels you’re not
            suitable to become a Driving Instructor or for any of the following reasons:</p>
          <ul>
            <li>You have been banned from driving</li>
            <li>You have 6 or more points on your licence</li>
            <li>You have any convictions for non-motoring offences</li>
            <li>Your application is likely to be declined if you have any of the following:</li>
            <li>You have been convicted of a violent, sexual, financial or drug-related crime.</li>
            <li>You have been banned from working with children.</li>
          </ul>
          <h2 style = "background-color:gray;">Step 2: The Training</h1>
          <p>You will need to undertake training with a Qualified Driving Instructor to be able to gain the necessary
            experience to pass your exams. Find out more about the process of training to become a driving instructor
            with PassVeryFast Driving School.</p>
          <p>There are 3 parts to becoming an <span class="caps">ADI</span>:</p>
          <h3>Part 1</h3>
          <p>The Approved Driving Instructor (ADI) Part 1 test is a theory test that takes around 1 hour 45 minutes and
            includes:</p>
          <h4>Multiple Choice Questions</h4>
          <p>You’ll be asked 100 questions in total, 25 in each of the following categories:</p>
          <ul>
            <li>Road procedure</li>
            <li>Traffic signs and signals, car control, pedestrians and mechanical knowledge</li>
            <li>Driving test, disabilities, and the law</li>
            <li>Publications and instructional techniques</li>
          </ul>
          <h4>Hazard Perception</h4>
          <p>First, you will be shown a video clip explaining how this test will work. It’s very similar to the learner
            hazard perception test that you can practise on our free guide.</p>
          <p>You will then be shown 14 videos that show every day road scenes. 13 of these clips will include 1 hazard
            and 1 clip will include 2 hazards.</p>
          <p>You can get up to 5 points per recognised hazard giving a maximum of 75 available points – you will need 57
            points to pass this test.</p>
          <p>You must pass both parts in the same sitting in order to pass the Part 1 exam. There is no limit to how
            many times you can take the test.</p>
          <p>Once you pass your Part 1 exam, you will then have 2 years available to qualify as an <span
              class="caps">ADI</span>. If you take more than 2 years you will need to take your Part 1 exam again.</p>
          <h3>Part 2</h3>
          <p>The Part 2 test takes around 1 hour and consists of the following</p>
          <h4>A Simple Eyesight Test</h4>
          <p>You will need to read a number plate from a distance of 26.5 metres for new-style number plate and 27.5
            metres for the old-style. You must use glasses or contact lenses during the whole test if you need them to
            read the number plate.</p>
          <h4>Vehicle Safety Questions</h4>
          <p>During this part of your test you will be asked 5 vehicle safety questions. This will consist of:</p>
          <ul>
            <li>3 ‘Show Me’ Questions</li>
            <li>2 ‘Tell Me’ Questions</li>
          </ul>
          <p>You will need to ‘Show’ the examiner and ‘Tell’ the examiner accordingly how to carry out specific vehicle
            checks. You will receive a minor fault for any questions you get wrong and a serious fault if you answer all
            5 questions incorrectly.</p>
          <p>You can view the questions here</p>
          <h4>Test of Driving Ability</h4>
          <p>This test is similar to the current driving test for learner drivers. Where possible, you will be driving
            in various road conditions, including Motorways, dual carriageways and differing traffic levels. You will
            need to be able to demonstrate the following to the examiner:</p>
          <ul>
            <li>Expert handling of the controls</li>
            <li>Ability to anticipate what other road users are doing and take appropriate actions.</li>
            <li>Correct road procedures</li>
            <li>Ability to judge speed, distance and timing</li>
            <li>Environmentally friendly driving</li>
            <li>Consideration and convenience of other road users</li>
          </ul>
          <h4>Manoeuvres</h4>
          <p>As with the learner driver test, you must show you are able to do a range of manoeuvres. Unlike the learner
            test, these are much more extensive and you will be asked to carry out a selection of the following:</p>
          <ul>
            <li>Parallel park</li>
            <li>Bay park</li>
            <li>Forward pay park</li>
            <li>Park on the right and reverse</li>
            <li>Emergency stop</li>
            <li>Move off straight ahead or at an angle</li>
          </ul>
          <h4>Independent Driving</h4>
          <p>As well as the above you will need to show that you can drive independently. This will be presented through
            a (roughly) twenty-minute drive where you will be instructed to either follow traffic signs, or a series of
            directions from a Sat Nav.</p>
          <p>On occasion, an examiner’s supervisor will be present at the test. They will not be assessing you or have
            any impact on your results but are instead there to assess the examiner’s performance.</p>
          <h3>Part 3</h3>
          <p>After completion of Parts 1 and 2, you will then be able to take your Part 3 test. Currently, this costs
            £111.</p>
          <p>The one hour test includes an assessment of:</p>
          <ul>
            <li>Lesson planning</li>
            <li>Risk management</li>
            <li>Teaching and learning strategies</li>
          </ul>
          <p>The examiner will test this by observing a “live” lesson given by you.</p>
          <h1 style = "background-color:gray;">Step 3: Apply for the Badge</h1>
          <p>Once you have passed your exams you can apply for your <span class="caps">ADI </span>(Approved Driving
            Instructor) Badge from the <span class="caps">DVSA</span>. The cost of this is £300 and you will need to pay
            by debit or credit card. The online service for this is open 6 am – 11:30 pm.</p>
          <p>The following requirements on the application are taken from the Gov website:</p>
          <ul>
            <li>Photocard Driving Licence:</li>
            <li>If you have a photocard driving licence you’ll need:</li>
            <li>Your online user ID from the Driver and Vehicle Standards Agency (DVSA)</li>
            <li>Your password</li>
            <li>Your driving licence number (if you’re giving <span class="caps">DVSA</span> permission to use your
              photocard photo)</li>
          </ul>
          <h4>Paper Driving Licence</h4>
          <p>If you have an old-style paper driving licence you’ll also need:</p>
          <ul>
            <li>To print the final screen and send it to <span class="caps">DVSA</span></li>
            <li>Passport-sized photo with your <span class="caps">ADI</span> personal reference number written on the
              back</li>
            <li>Your user ID and password</li>
            <li>Your user ID can be found on all letters sent to you by <span class="caps">DVSA</span>. It’s not the
              same as your <span class="caps">ADI</span> personal reference number.</li>
          </ul>
          <p>Email <span class="caps">DVSA</span> if you’ve never had, or have lost, your user ID. You must include
            your:</p>
          <ul>
            <li>Driving licence number</li>
            <li><span class="caps">ADI</span> number</li>
            <li>Date of birth</li>
          </ul>
          <p>Upon completion and acknowledgement of this application – you can look forward to your new career as a
            fully Qualified Driving Instructor!</p>
          <p>Call our dedicated Training Team on 0800 710 1132 to become driving instructor today!. We’ll look forward
            to hearing from you.</p>
